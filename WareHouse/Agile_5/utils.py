'''
This file contains utility functions used in the application.
'''
def calculate_sum(a, b):
    '''
    Calculates the sum of two numbers.
    Parameters:
        a (int): First number
        b (int): Second number
    Returns:
        int: Sum of a and b
    '''
    return a + b
def calculate_product(a, b):
    '''
    Calculates the product of two numbers.
    Parameters:
        a (int): First number
        b (int): Second number
    Returns:
        int: Product of a and b
    '''
    return a * b